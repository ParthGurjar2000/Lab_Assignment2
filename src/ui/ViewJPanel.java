    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        model.setRowCount(0);
        for (Employee em : history.getHistory()) {

            Object[] row = new Object[4];
            row[0] = em;
            row[1] = em.getName();
            row[2] = em.getPositonTitle();
            row[3] = em.getEmailAddress();

            model.addRow(row);
        }

    }

